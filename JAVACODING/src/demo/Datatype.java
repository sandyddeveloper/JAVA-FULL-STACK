package demo;

public class Datatype {
	public static void main(String[] args) {
		int  a = 46;
		float b = 5.5f;
		double c = 5.50000;
		
		char d = 'D'; // only single char allows 
		String e = "Dhoni";// for multiple char
		
		// 10/3 = 3.3333333 for float
		// 10/3 = 3.33333333333333........ for double
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}
